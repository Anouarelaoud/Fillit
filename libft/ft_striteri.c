#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int				n;
	unsigned int	i;

	if (s && f)
	{
		n = 0;
		i = 0;
		while (s[i])
		{
			f(i, &s[i]);
			i++;
			n++;
		}
	}
}
