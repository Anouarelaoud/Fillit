#include "libft.h"

char	*ft_strcat(char *dest, char *src)
{
	char *res;

	res = (char *)dest;
	while (*dest)
		dest++;
	while (*src)
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (res);
}
